model_name = "google/flan-t5-base"
tokenizer = T5Tokenizer.from_pretrained(model_name)
model = T5ForConditionalGeneration.from_pretrained(model_name)
device = torch.device("cuda" if torch.cuda.is_available() else "cpu")
model.to(device)



def detect_stance(x: str, t: str, prompt: str, decoding_strategy: str = "greedy") -> str:
    # Preprocess the tweet
    x_cleaned = preprocess_tweet(x)
    input_text = prompt.format(x=x_cleaned, t=t)
    input_ids = tokenizer.encode(input_text, return_tensors="pt", max_length=128, truncation=True).to(device)

    if decoding_strategy == "greedy":
        outputs = model.generate(input_ids, max_length=16, do_sample=False)
    else:  # beam search
        outputs = model.generate(
            input_ids,
            max_length=16,
            num_beams=4,               # More beams = better quality (optional tweak)
            early_stopping=True,
            num_return_sequences=1     # Only keep best beam
        )

    pred = tokenizer.decode(outputs[0], skip_special_tokens=True).strip().upper()

    # Normalize prediction to standard labels
    if "FAVOR" in pred:
        return "FAVOR"
    elif "AGAINST" in pred:
        return "AGAINST"
    elif "NEUTRAL" in pred:
        return "NEUTRAL"
    else:
        return "NEUTRAL"  # Fallback





