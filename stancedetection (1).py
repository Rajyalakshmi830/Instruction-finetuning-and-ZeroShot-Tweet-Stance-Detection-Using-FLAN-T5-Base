from sklearn.metrics import accuracy_score

# Evaluate on all test datasets (zero-shot testing)
def evaluate_on_datasets(prompts: List[str], datasets: Dict[str, str], decoding_strategy: str = "greedy"):
    results = {}
    for dataset_name, path in datasets.items():
        test_df = pd.read_csv(path)
        true_labels = test_df['Stance'].tolist()
        all_predictions = {prompt: [] for prompt in prompts}

        for x, t in zip(test_df['Tweet'], test_df['Target']):
            for prompt in prompts:
                pred = detect_stance(x, t, prompt, decoding_strategy)
                all_predictions[prompt].append(pred)

        # Calculate performance metrics without accuracy
        for prompt, predictions in all_predictions.items():
            report = classification_report(true_labels, predictions, output_dict=True, labels=["FAVOR", "AGAINST", "NEUTRAL"])
            results[f"{dataset_name}{prompt[:50]}...{decoding_strategy}"] = {
                "precision": report["weighted avg"]["precision"],
                "recall": report["weighted avg"]["recall"],
                "f1": report["weighted avg"]["f1-score"],
                "macro_f1": report["macro avg"]["f1-score"],
                "micro_f1": report["micro avg"]["f1-score"],
                "weighted_f1": report["weighted avg"]["f1-score"],
                "accuracy": accuracy_score(true_labels, predictions)
            }
            # Generate confusion matrix visualization
            cm = confusion_matrix(true_labels, predictions, labels=["FAVOR", "AGAINST", "NEUTRAL"])
            plt.figure(figsize=(4, 4))
            sns.heatmap(cm, annot=True, fmt='d', cmap='Blues')
            plt.title(f"Confusion Matrix - {dataset_name} - {prompt[:50]}... ({decoding_strategy})")
            plt.ylabel('True Label')
            plt.xlabel('Predicted Label')
            plt.savefig(f"confusion_matrix_{dataset_name}{prompt[:10]}{decoding_strategy}.png")
            plt.close()
    return results

