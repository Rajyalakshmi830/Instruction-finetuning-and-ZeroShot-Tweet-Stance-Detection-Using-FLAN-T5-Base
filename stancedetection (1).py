import re

# Preprocessing function for tweets
def preprocess_tweet(text: str) -> str:
    # Convert to lowercase
    text = text.lower()
    # Remove URLs
    text = re.sub(r'http\S+|www\S+|https\S+', '', text, flags=re.MULTILINE)
    # Remove mentions and hashtags
    text = re.sub(r'@\w+|\#', '', text)
    # Remove special characters and emojis
    text = re.sub(r'[^\w\s]', '', text)  # Keeps only words and spaces
    # Remove extra whitespace
    text = ' '.join(text.split())
    # Expand basic contractions (optional, simple approach)
    text = text.replace("don't", "do not").replace("won't", "will not").replace("can't", "cannot")
    return text







